/**
 * Squared exponential (RBF) kernel
 * k(x, x') = σ² * exp(-||x - x'||² / (2 * l²))
 */
static double kernel(const double* x1, const double* x2, int dim) {
    double sq_dist = 0.0;
    for (int i = 0; i < dim; i++) {
        double diff = x1[i] - x2[i];
        sq_dist += diff * diff;
    }
    
    double l2 = g_model.length_scale * g_model.length_scale;
    return g_model.signal_variance * exp(-sq_dist / (2.0 * l2));
}

/**
 * Initialize model with synthetic training data
 * In production, this would load serialized model parameters
 */
int gp_init(void) {
    if (g_model.initialized) {
        return 0;  /* Already initialized */
    }
    
    /* Set hyperparameters */
    g_model.length_scale = GP_LENGTH_SCALE;
    g_model.signal_variance = GP_SIGNAL_VARIANCE;
    g_model.noise_variance = GP_NOISE_VARIANCE;
    
    /* Generate synthetic training data (would be loaded from file in production) */
    /* This simulates a Latin hypercube sample in [0, 1]^5 */
    for (int i = 0; i < GP_N_TRAIN; i++) {
        for (int j = 0; j < GP_INPUT_DIM; j++) {
            /* Deterministic pseudo-random for reproducibility */
            g_model.X_train[i][j] = (double)((i * 7 + j * 13) % 100) / 100.0;
        }
        
        /* Synthetic alpha coefficients (would come from K^{-1} y) */
        g_model.alpha[i] = (double)((i * 11) % 100 - 50) / 100.0;
    }
    
    g_model.initialized = 1;
    return 0;
}

/**
 * Make a GP prediction
 * 
 * Computes:
 *   mean = k(x, X)^T @ alpha
 *   variance = k(x, x) - k(x, X)^T @ K^{-1} @ k(x, X)
 * 
 * Note: This is a simplified implementation. Production code would:
 * - Use pre-computed Cholesky decomposition
 * - Optimize memory access patterns
 * - Handle numerical stability
 */
int gp_predict(const double* input, GPPrediction* output) {
    if (!g_model.initialized) {
        return -1;  /* Not initialized */
    }
    
    if (input == NULL || output == NULL) {
        return -2;  /* Invalid arguments */
    }
    
    uint64_t start_time = get_time_us();
    
    /* Compute kernel vector k(x, X) */
    double k_star[GP_N_TRAIN];
    for (int i = 0; i < GP_N_TRAIN; i++) {
        k_star[i] = kernel(input, g_model.X_train[i], GP_INPUT_DIM);
    }
    
    /* Compute mean: k(x, X)^T @ alpha */
    double mean = 0.0;
    for (int i = 0; i < GP_N_TRAIN; i++) {
        mean += k_star[i] * g_model.alpha[i];
    }
    
    /* Compute variance (simplified - full implementation would use L solve) */
    /* k(x, x) = signal_variance (for RBF kernel with x == x) */
    double k_xx = g_model.signal_variance;
    
    /* Approximate variance using diagonal dominance assumption */
    double var_reduction = 0.0;
    for (int i = 0; i < GP_N_TRAIN; i++) {
        var_reduction += k_star[i] * k_star[i] / (g_model.signal_variance + g_model.noise_variance);
    }
    double variance = k_xx - var_reduction;
    if (variance < 0.0) variance = g_model.noise_variance;  /* Numerical floor */
    
    /* Fill output */
    output->mean[0] = mean;
    output->variance[0] = variance;
    output->computation_time_us = get_time_us() - start_time;
    
    return 0;
}

/**
 * Validate model state
 */
GPValidation gp_validate(void) {
    GPValidation result;
    
    if (!g_model.initialized) {
        result.valid = 0;
        result.message = "Model not initialized";
        return result;
    }
    
    /* Check hyperparameters are reasonable */
    if (g_model.length_scale <= 0.0) {
        result.valid = 0;
        result.message = "Invalid length scale";
        return result;
    }
    
    if (g_model.signal_variance <= 0.0) {
        result.valid = 0;
        result.message = "Invalid signal variance";
        return result;
    }
    
    result.valid = 1;
    result.message = "Model validated successfully";
    return result;
}

